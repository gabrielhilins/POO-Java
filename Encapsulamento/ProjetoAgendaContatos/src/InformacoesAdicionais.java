public class InformacoesAdicionais {
    String endereco;
    String instagram;
    String linkedin;
    int idade;
    String naturalidade;
    String dataAniversario;

    public InformacoesAdicionais(String endereco, String instagram, String linkedin, int idade, String naturalidade, String dataAniversario) {
        this.endereco = endereco;
        this.instagram = instagram;
        this.linkedin = linkedin;
        this.idade = idade;
        this.naturalidade = naturalidade;
        this.dataAniversario = dataAniversario;
    }
}
